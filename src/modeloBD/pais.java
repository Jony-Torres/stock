
package modeloBD;

/**
 *
 * @author Jony
 */
public class pais {
    Integer cod_pais;
    String des_pais;

    public pais() {
        cod_pais = null;
        des_pais = null;
    }

    public Integer getCod_pais() {
        return cod_pais;
    }

    public void setCod_pais(Integer cod_pais) {
        this.cod_pais = cod_pais;
    }

    public String getDes_pais() {
        return des_pais;
    }

    public void setDes_pais(String des_pais) {
        this.des_pais = des_pais;
    }
    
}
