    private void carga_fecha() {
        try {
            Date hoy = new Date();
            Date fecha = null;
            SimpleDateFormat formatofecha = new SimpleDateFormat("dd/MM/yyyy");
            String fechaact = formatofecha.format(hoy);
            fecha = formatofecha.parse(fechaact);
            txt_fecha_comprobante.setDate(fecha);
        } catch (ParseException ex) {
            Logger.getLogger(controlador_persona.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

