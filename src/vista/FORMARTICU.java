    private void txt_nro_loteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_nro_loteKeyTyped
        char tecla;
        int num=20;
        tecla=evt.getKeyChar();
        if (!Character.isDigit(tecla)&&tecla!=KeyEvent.VK_BACK_SPACE) {
           evt.consume();
           getToolkit().beep();
        } 
        if(txt_nro_lote.getText().length()>=num){
            evt.consume();
        }
    }//GEN-LAST:event_txt_nro_loteKeyTyped

    private void txt_precio_compraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_precio_compraKeyTyped
        char tecla;
        int num = 15;
        tecla = evt.getKeyChar();
        //Validacion de solo numero o decimales
        if(((tecla < '0') || (tecla > '9')) && (tecla != KeyEvent.VK_BACK_SPACE) && (tecla !='.')){
            evt.consume();
            getToolkit().beep();
        }
        if (txt_precio_compra.getText().length() >= num) {
            evt.consume();
        }
    }//GEN-LAST:event_txt_precio_compraKeyTyped

