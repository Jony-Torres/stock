    public void carga_combobox() {
        //CARGA ESTADO
        Object tpd[] = new Object[2];
        tpd[0] = "ACTIVO";
        tpd[1] = "INACTIVO";
        DefaultComboBoxModel dcb = new DefaultComboBoxModel(tpd);
        this.cmb_estado.setModel(dcb);
        String std = "ACTIVO";
        this.cmb_estado.setSelectedItem(std);
        //CARGA INDICADOR DE CONTROL DE STOCK
        Object tpu[] = new Object[2];
        tpu[0] = "SI";
        tpu[1] = "NO";
        DefaultComboBoxModel dcu = new DefaultComboBoxModel(tpu);
        this.cmb_man_stock.setModel(dcu);
        String upd = "SI";
        this.cmb_man_stock.setSelectedItem(upd);
        
    }

