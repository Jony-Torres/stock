    private void txt_nuevo_pasKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_nuevo_pasKeyTyped
        int num=10;
            if(txt_nuevo_pas.getText().length()>=num){
                evt.consume();
            }
            char charecter = evt.getKeyChar();
            if (Character.isLowerCase(charecter)) {
		evt.setKeyChar(Character.toUpperCase(charecter));
            }
    }//GEN-LAST:event_txt_nuevo_pasKeyTyped

