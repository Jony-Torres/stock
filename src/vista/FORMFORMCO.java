    private void txt_importeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_importeKeyTyped
        char tecla;
        int num = 15;
        tecla = evt.getKeyChar();
        //Validacion de solo numero o decimales
        if(((tecla < '0') || (tecla > '9')) && (tecla != KeyEvent.VK_BACK_SPACE) && (tecla !='.')){
            evt.consume();
            getToolkit().beep();
        }
        if (txt_importe.getText().length() >= num) {
            evt.consume();
        }
    }//GEN-LAST:event_txt_importeKeyTyped

