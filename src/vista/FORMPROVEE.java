    private void txt_cod_personaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cod_personaKeyTyped
        char tecla;
        int num=15;
        tecla=evt.getKeyChar();
        if (!Character.isDigit(tecla)&&tecla!=KeyEvent.VK_BACK_SPACE) {
           evt.consume();
           getToolkit().beep();
        } 
        if(txt_cod_persona.getText().length()>=num){
            evt.consume();
        }
    }//GEN-LAST:event_txt_cod_personaKeyTyped

