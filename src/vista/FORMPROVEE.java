    public void inicializa_controles() {
        bloquea_campos();
        bloquea_botones();
        carga_combobox();
        ocultar_label();
    }

    private void bloquea_campos() {
        txt_codigo.setEditable(false);
        txt_cod_persona.setEditable(false);
        txt_nomb_persona.setEditable(false);
        txt_tipo_prov.setEditable(false);
        txt_desc_tipo_prov.setEditable(false);
        btn_busq_cod_pers.setEnabled(false);
        btn_busq_tip_prov.setEnabled(false);
        cmb_estado.setEnabled(false);
    }
    public void habilita_campos() {
        //txt_codigo.setEditable(false);
        txt_cod_persona.setEditable(true);
        txt_nomb_persona.setEditable(true);
        txt_tipo_prov.setEditable(true);
        txt_desc_tipo_prov.setEditable(true);
        cmb_estado.setEnabled(true);
        btn_busq_cod_pers.setEnabled(true);
        btn_busq_tip_prov.setEnabled(true);
        txt_cod_persona.requestFocus();
    }
    public void limpia_campos() {
        txt_codigo.setText("");
        txt_cod_persona.setText("");
        txt_nomb_persona.setText("");
        txt_tipo_prov.setText("");
        txt_desc_tipo_prov.setText("");
        txt_error.setText("");
    }
    private void bloquea_botones() {
        btn_actualizar.setEnabled(false);
        btn_eliminar.setEnabled(false);
        btn_guardar.setEnabled(false);
        btn_ejecutar.setEnabled(false);
        btn_persona.setEnabled(false);
        btn_busqueda.setEnabled(true);
        btn_nuevo.setEnabled(true);
        btn_nuevo.requestFocus();
    }

    private void carga_combobox() {
        Object tpd[] = new Object[2];
        tpd[0] ="ACTIVO";
        tpd[1] ="INACTIVO";
        DefaultComboBoxModel dcb = new DefaultComboBoxModel(tpd);
        this.cmb_estado.setModel(dcb);
        //cmb_estado.addItem("Seleccione Opcion");
        String std = "ACTIVO";
        this.cmb_estado.setSelectedItem(std);
    }

    private void ocultar_label() {
        lblcod.setVisible(false);
        lblcodpers.setVisible(false);
        lblest.setVisible(false);
        campo_obligatorio.setVisible(false);
    }
    public void mostrar_campos_requeridos() {
        lblcodpers.setVisible(true);
        lblest.setVisible(true);
        campo_obligatorio.setVisible(true);
        campo_obligatorio.setText("CAMPOS OBLIGATORIOS * ");
        lblest.setForeground(new Color(255,51,51));
        lblcodpers.setForeground(new Color(255,51,51));
        campo_obligatorio.setForeground(new Color(255,51,51));
    }
