    private void txt_cod_cajaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cod_cajaKeyTyped
        int num=10;
        if(txt_cod_caja.getText().length()>=num){
            evt.consume();
        }
        char charecter = evt.getKeyChar();
            if (Character.isLowerCase(charecter)) {
		evt.setKeyChar(Character.toUpperCase(charecter));
		}
    }//GEN-LAST:event_txt_cod_cajaKeyTyped

